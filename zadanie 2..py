# Pracuję na PyCharmie

# Aby skonfigurować IDE:

# 1. Otwórz projekt w PyCharm.

# 2. Przejdź do menu "File" -> "Settings"

# 3. W ustawieniach, przejdź do sekcji "Project: Kurs" -> "Python Interpreter".

# 4. Kliknij ikonę koła zębatego obok rozwijalnej listy interpreterów Pythona i wybierz "Add..." (Dodaj).

# 5. Wybierz opcję "Pipenv Environment" z listy dostępnych opcji.

# 6. W nowym oknie dialogowym, wybierz istniejące wirtualne środowisko.

# 7. Po wybraniu odpowiedniego środowiska, kliknij "Ok" aby zatwierdzić.